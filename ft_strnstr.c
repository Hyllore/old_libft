#include "libft.h"

char	*ft_strnstr(const char *s1, const char *s2, size_t n)
{
	int	i;
	int	i2;
	int	b;

	b = (int)n;
	if (s2[0] == '\0')
		return ((char*)s1);
	while (s1[0] != '\0' && b > 0)
	{
		i = 0;
		i2 = 0;
		while ((b > 0) && s1[i] != '\0' && (s1[i] == s2[i2]))
		{
			if (s2[++i2] == '\0')
				return ((char *)s1);
			i += 1;
			b -= 1;
		}
		b -= 1;
		s1++;
	}
	return (NULL);
}
